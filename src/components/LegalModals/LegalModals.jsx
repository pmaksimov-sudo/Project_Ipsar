/* ───── Рендер строки с кликабельными ссылками ───── */
const URL_REGEX = /(https?:\/\/[^\s]+)/g

function renderLine(line) {
  const parts = line.split(URL_REGEX)
  return parts.map((part, i) =>
    URL_REGEX.test(part)
      ? <a key={i} href={part} target="_blank" rel="noopener noreferrer" style={{ color: 'var(--color-accent)', textDecoration: 'underline' }}>{part}</a>
      : part
  )
}

/* ───── Общий попап ───── */
function LegalModal({ title, sections, onClose }) {
  useEffect(() => {
    const onKey = e => e.key === 'Escape' && onClose()
    window.addEventListener('keydown', onKey)
    return () => window.removeEventListener('keydown', onKey)
  }, [onClose])

  return (
    <div className={styles.overlay} onClick={onClose}>
      <div className={styles.modal} onClick={e => e.stopPropagation()}>
        <div className={styles.header}>
          <div>
            <p className={styles.title}>{title}</p>
            <p className={styles.meta}>ООО «МНПФ СПЕЦМЕДТЕХНИКА» · Редакция 2024 года</p>
          </div>
          <button className={styles.close} onClick={onClose} aria-label="Закрыть">✕</button>
        </div>
        <div className={styles.body}>
          {sections.map((s, i) => (
            <div key={i} className={styles.section}>
              <p className={styles.sectionTitle}>{s.heading}</p>
              {s.text.split('\n').map((line, j) => (
                <p key={j} className={styles.sectionText}>{renderLine(line)}</p>
              ))}
            </div>
          ))}
        </div>
      </div>
    </div>
  )
}

